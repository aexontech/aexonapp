// Search by code prefix or display text (case-insensitive)
export function searchICD10(query: string, limit = 10): ICD10Entry[] {
  if (!query || query.length < 2) return [];
  const q = query.toLowerCase();
  const results: ICD10Entry[] = [];
  for (const entry of ICD10_DATA) {
    if (entry.code.toLowerCase().startsWith(q) || entry.display.toLowerCase().includes(q)) {
      results.push(entry);
      if (results.length >= limit) break;
    }
  }
  return results;
}

export function getICD10ByCode(code: string): ICD10Entry | undefined {
  return ICD10_DATA.find(e => e.code === code);
}

